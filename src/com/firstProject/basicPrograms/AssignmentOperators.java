package com.firstProject.basicPrograms;

public class AssignmentOperators {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		num1 += num2;
		System.out.println("Print num1 : "+num1);
		num1 -= num2;
		System.out.println("Print num1 2nd iteration : "+num1);
		num1 *= num2;
		System.out.println("Print num1 3rd iteration : "+num1);
		num1 /= num2;
		System.out.println("Print num1 4th iteration : "+num1);
		num1 %= num2;
		System.out.println("Print num1 5th iteration : "+num1);

	}
}
