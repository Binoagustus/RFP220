package com.firstProject.basicPrograms;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		int sub_value = num1 - num2;
		System.out.println("Subtracted value is :" +sub_value);
		int mult_value = num1 * num2;
		System.out.println("Multiplied value is :" +mult_value);
		int div_value = num1 / num2;
		System.out.println("Divided value is :" +div_value);
		int mod_value = num1 % num2;
		System.out.println("Subtraction value is :" +mod_value);
		
	}
}
